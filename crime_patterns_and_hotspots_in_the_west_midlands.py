center_lon = clustered_df['Longitude'].mean()

# Create a Folium map object
west_midlands_map = folium.Map(location=[center_lat, center_lon], zoom_start=10)

"""## Adding Crime Hotspots to Map

### Subtask:
Iterate through the `clustered_df` (excluding noise points) and add markers for each crime hotspot to the Folium map, coloring them by their cluster ID. Consider adding pop-up information if relevant.

"""

from folium.plugins import MarkerCluster
import branca.colormap as cm
import matplotlib.cm as mpcm
import matplotlib.colors as mcolors

# Get unique cluster IDs
unique_clusters = clustered_df['cluster'].unique()

# Create a colormap using matplotlib for distinct colors for each cluster
num_clusters = len(unique_clusters)

# Choose a qualitative colormap from matplotlib based on the number of unique clusters
# 'tab10' has 10 colours, 'tab20' has 20 colors.
# If more than 20 clusters, use a perceptually uniform colormap like 'viridis'
# and sample it to get distinct colors.
if num_clusters <= 10:
    cmap_mp = mpcm.get_cmap('tab10', num_clusters)
elif num_clusters <= 20:
    cmap_mp = mpcm.get_cmap('tab20', num_clusters)
else:
    # For a larger number of clusters, sample 'viridis' to ensure distinctness
    cmap_mp = mpcm.get_cmap('viridis', num_clusters)

# Generate hex colors from the chosen colormap
colors_hex = [mcolors.to_hex(cmap_mp(i)) for i in range(num_clusters)]

# Create the color_map dictionary
color_map = {cluster_id: colors_hex[i] for i, cluster_id in enumerate(unique_clusters)}

print("Setup complete: MarkerCluster imported, unique cluster IDs identified, and color map created.")

for cluster_id in unique_clusters:
    # Filter data for the current cluster
    cluster_data = clustered_df[clustered_df['cluster'] == cluster_id]

    # Create a MarkerCluster for the current cluster
    marker_cluster = MarkerCluster(name=f'Cluster {cluster_id}').add_to(west_midlands_map)

    # Get the color for the current cluster
    current_color = color_map[cluster_id]

    # Add CircleMarkers for each crime incident in the cluster
    for idx, row in cluster_data.iterrows():
        folium.CircleMarker(
            location=[row['Latitude'], row['Longitude']],
            radius=3, # Small radius for individual points
            color=current_color,
            fill=True,
            fill_color=current_color,
            fill_opacity=0.7,
            popup=f"Cluster ID: {row['cluster']}<br>Crime ID: {row['Crime ID']}"
        ).add_to(marker_cluster)

# Add LayerControl to the map
folium.LayerControl().add_to(west_midlands_map)

print("Crime hotspots added to the map with distinct colors and LayerControl.")

